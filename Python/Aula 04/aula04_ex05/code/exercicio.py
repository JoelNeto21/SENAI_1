for i in range(5, 101):
    if i % 7 == 0 and i % 5 != 0:
        print(i)

'''
DESCRIÇÃO:
- O programa percorre os números de 5 a 100 usando um laço for.
- Em cada iteração, verifica se o número é divisível por 7 e não é múltiplo de 5.
- Se a condição for satisfeita, o número é exibido na tela.
- O resultado final é uma sequência de números entre 5 e 100 que atendem a esses critérios.
'''