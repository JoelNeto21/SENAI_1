for num in range(1, 101):
    if num % 2 == 0:
        print(num)

'''
DESCRIÇÃO:
- O programa usa um laço for para iterar os números de 1 a 100.
- Para cada número, verifica se o resto da divisão por 2 (% 2) é zero, indicando que é par.
- Quando a condição é verdadeira, o número é exibido na tela.
- Assim, são mostrados todos os números pares entre 1 e 100.
'''